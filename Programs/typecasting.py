# Typecasting is the process of converting a variable from one datatype to another

integer_data = 20
float_data = 99.99
boolean_data = True
string_data = "Hello"

# print(type(integer_data))

print(float(integer_data))
print(int(float_data))
print(str(integer_data))
print(str(boolean_data))
print(bool(string_data))